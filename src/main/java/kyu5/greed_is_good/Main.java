    //
    // Три единицы => 1000 баллов
    // Три шестерки => 600 баллов
    // Три пятерки => 500 баллов
    // Три четверки => 400 баллов
    // Три тройки => 300 баллов
    // Три двойки => 200 баллов
    // Один 1 => 100 баллов
    // Один 5 => 50 баллов
    //
    //Один кубик может быть подсчитан только один раз в каждом броске. Например, данная «5» может учитываться
    // только как часть тройки (составляющей 500 баллов) или как отдельные 50 баллов, но не оба в одном броске.
    //
    //Пример подсчета очков
    //
    //Оценка броска
    // ----------------------- ------------------
    // 5 1 3 4 1 => 250: 50 (для 5) + 2 * 100 (для 1)
    // 1 1 1 3 1 => 1100: 1000 (для трех единиц) + 100 (для остальных 1)
    // 2 4 4 5 4 => 450: 400 (за три четверки) + 50 (за пятерки)
    //
