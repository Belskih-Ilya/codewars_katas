    public static int greedy(int[] dice) {
        int resScore = 0;
        for (int i = 1; i < 7; i++) {
            int count = 0;
            for (int j = 0; j < dice.length; j++) {
                if (i == dice[j]) count++;
            }
            if (i == 1) {
                if (count < 3) {
                    resScore += count*100;
                } else if (count == 3) {
                    resScore += 1000;
                } else {
                    resScore += (1000 + ((count-3)*100));
                }
            } else if (i == 5) {
                if (count < 3) {
                    resScore += count*50;
                } else if (count == 3) {
                    resScore += 500;
                } else {
                    resScore += (500 + ((count-3)*50));
                }
            } else if (i == 2) {
                if (count >= 3) {
                    resScore += 200;
                }
            } else if (i == 3) {
                if (count >= 3) {
                    resScore += 300;
                }
            } else if (i == 4) {
                if (count >= 3) {
                    resScore += 400;
                }
            } else if (i == 6) {
                if (count >= 3) {
                    resScore += 600;
                }
            }
        }
        return resScore;
    }
